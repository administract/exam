public class Paper {
    private int paperId;
    private String title;

    public int getPaperId() {
        return paperId;
    }

    public String getTitle() {
        return title;
    }

    public void setPaperId(int paperId) {
        this.paperId = paperId;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
